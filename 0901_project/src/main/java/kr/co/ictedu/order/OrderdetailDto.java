package kr.co.ictedu.order;

public class OrderdetailDto {
	
	private String detailorder_no;
	private String detailorder_id;
	private String detailorder_title;
	private String detailorder_ticketNumber;
	private String detailorder_pay;
	
	
	
	

	public String getDetailorder_no() {
		return detailorder_no;
	}

	public void setDetailorder_no(String detailorder_no) {
		this.detailorder_no = detailorder_no;
	}

	public String getDetailorder_id() {
		return detailorder_id;
	}

	public void setDetailorder_id(String detailorder_id) {
		this.detailorder_id = detailorder_id;
	}

	public String getDetailorder_title() {
		return detailorder_title;
	}

	public void setDetailorder_title(String detailorder_title) {
		this.detailorder_title = detailorder_title;
	}

	public String getDetailorder_ticketNumber() {
		return detailorder_ticketNumber;
	}

	public void setDetailorder_ticketNumber(String detailorder_ticketNumber) {
		this.detailorder_ticketNumber = detailorder_ticketNumber;
	}

	public String getDetailorder_pay() {
		return detailorder_pay;
	}

	public void setDetailorder_pay(String detailorder_pay) {
		this.detailorder_pay = detailorder_pay;
	}

	
}
